package outros;
import java.nio.file.FileSystemException;

public class IncrementoDecremento {

	public static void main(String[] args) {
		
		int a = 1;
		
		System.out.println(a++);
		System.out.println(a);
		
		System.out.println(++a);
		System.out.println(a);
		
		System.out.println(a--);
		System.out.println(a);
		
		System.out.println(--a);
		System.out.println(a);
		

	}

}
