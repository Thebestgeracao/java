package outros;

public class ComandoDeSelecao {

	public static void main(String[] args) {
		
		int escolha = 3;
		switch(escolha) {
		case 1:
			System.out.println("Número 1");
			break;
		case 2:
			System.out.println("Número 2");
			break;
		case 3:
			System.out.println("Número 3");
			break;
		default:
			System.out.println("Outro número");
		}
		

	}

}
